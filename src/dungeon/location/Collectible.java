package dungeon.location;

/**
 * Collectibles are the perks associated with each cave, they can only be found in a cave and in a
 * tunnel. There are a total of 4 types of collectibles available namely, Diamonds, Rubies,
 * Sapphires and Arrows. The users can specify the percentage of caves that can contain treasures
 * and the total percentage of locations that can contain arrows with command line parameters.
 */
public enum Collectible {

    DIAMONDS, RUBIES, SAPPHIRES, ARROW;

}
